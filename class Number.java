class Number
{
    int num;
    Number()
    {
        num=10;
    }
    Number(int a)
    {
        num=a;
    }
}
class CDemo
{
    public static void main(String args[])
    {
        Number obj1=new Number ();
        Number obj2=new Number(20);
        System.out.println(obj1.num+""+obj2.num);
    }
}